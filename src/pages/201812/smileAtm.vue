<template>
  <div
    :style="style.root"
    class="root"
  >
    <!-- photo :src="baseUrl + '666.png'+ this.$qiniuCompress()" -->
    <img
      v-if="photo !== null"
      :src="photo + this.$qiniuCompress()"
      :class="{'x-photo':iphoneX,'photo':!iphoneX}"
      class="photo"
    >
    <!-- 二维码 -->
    <img
      :src="qrcodeImg+ this.$qiniuCompress()"
      :class="{'x-ewm':iphoneX,'ewm':!iphoneX}"
      class="ewm"
    >
    <!-- 已使用 -->
    <img
      v-show="hasUsed"
      :src="baseUrl + 'hasUsed.png'+ this.$qiniuCompress()"
      :class="{'x-hasUsed':iphoneX,'hasUsed':!iphoneX}"
      class="hasUsed"
    >
  </div>
</template>
<script>
import { onlyGetPhoto } from "../../mixins/onlyGetPhoto";
import {
  $wechat,
  isInWechat,
  wechatShareTrack,
  Cookies,
  sendCoupon,
  checkGetCoupon,
  dateFormat,
  formatTimestamp
} from 'services'
const cdnUrl = process.env.CDN_URL;
export default {
  mixins: [onlyGetPhoto],
  data() {
    return {
      baseUrl: cdnUrl + "/fe/marketing/img/simle_atm/",
      style: {
        root: {
          height: this.$innerHeight() + "px"
        }
      },
      photo: null,
      iphoneX: false,
      coupon_batch_id: this.$route.query.coupon_batch_id,
      id: this.$route.query.id,
      oid: this.$route.query.utm_source,
      couponID: ['111', '112', '113', '114'],
      new_coupon_batch_id: this.$route.query.coupon_batch_id,
      qrcodeImg: null,
      hasUsed: false,
      params: {
        user_id: null
      },
      wxShareInfoValue: {
        title: "欢度双十二，连卡福邀你“刷脸”赢好礼！",
        desc: "错过今天，再等N年。",
        link: "http://papi.xingstation.com/api/s/3QQ" + window.location.search,
        //link: "http://papi.newgls.cn/api/s/JZo" + window.location.search,
        imgUrl: cdnUrl + "/fe/marketing/img/simle_atm/icon.jpg",
        success: () => {
          wechatShareTrack();
        }
      }
    };
  },
  mounted() {
    //微信授权
    if (isInWechat() === true) {
      if (
        process.env.NODE_ENV === 'production' ||
        process.env.NODE_ENV === 'testing'
      ) {
        this.handleWechatAuth()
      }
    }
    if (this.$innerHeight() > 672) {
      this.iphoneX = true;
    } else {
      this.iphoneX = false;
    }

  },
  methods: {
    //微信静默授权
    handleWechatAuth() {
      if (Cookies.get('sign') === null) {
        let base_url = encodeURIComponent(String(window.location.href))
        let redirct_url =
          process.env.WX_API +
          '/wx/officialAccount/oauth?url=' +
          base_url +
          '&scope=snsapi_base'
        window.location.href = redirct_url
      } else {
        this.randomCouponID()
        this.wxShareInfoValue.link = this.changeUrlArg(this.wxShareInfoValue.link, 'coupon_batch_id', this.new_coupon_batch_id)
        this.handleShare()
        this.userId = Cookies.get('user_id')
        this.params.user_id = this.userId
        this.checkCouponIsUse()

      }
    },
    handleShare() {
      $wechat()
        .then(res => {
          res.share(this.wxShareInfoValue)
        })
        .catch(_ => {
          console.warn(_.message)
        })
    },
    //分享的链接处理函数
    changeUrlArg(url, arg, val) {
      let pattern = arg + '=([^&]*)';
      let replaceText = arg + '=' + val;
      return url.match(pattern) ? url.replace(eval('/(' + arg + '=)([^&]*)/gi'), replaceText) : (url.match('[\?]') ? url + '&' + replaceText : url + '?' + replaceText);
    },
    //随机出randomCouponID
    randomCouponID() {
      let that = this
      that.new_coupon_batch_id = that.couponID[Math.floor(Math.random() * that.couponID.length)]
      // for (let i = 0; i < this.couponID.length; i++) {
      //   if (parseInt(this.couponID[i]) !== this.new_coupon_batch_id) {
      //     this.new_coupon_batch_id = this.couponID[i]
      //     break;
      //   }
      // }
    },
    //判断是否领过优惠券
    checkCouponIsUse() {
      let args = {
        coupon_batch_id: this.coupon_batch_id,
        include: 'couponBatch',
        qiniu_id: this.id
      }
      checkGetCoupon(args)
        .then(res => {
          console.log('checkGetCoupon', res)
          if (res) {
            this.handleData(res)
          } else {
            let data = new Date()
            args = {
              coupon_batch_id: this.coupon_batch_id,
              include: 'couponBatch'
            }
            args.start_date = dateFormat(
              new Date(formatTimestamp(data, true)),
              'yyyy-MM-dd hh:mm:ss'
            )
            args.end_date = dateFormat(
              new Date(formatTimestamp(data, false) - 1000),
              'yyyy-MM-dd hh:mm:ss'
            )
            checkGetCoupon(args)
              .then(res => {
                console.log('checkGetCoupon', res)
                if (res) {
                  this.handleData(res)
                } else {
                  this.sendCoupon()
                }
              })
              .catch(err => {
                console.log(err)
              })
          }
        })
        .catch(err => {
          console.log(err)
        })
    },
    //发优惠券
    sendCoupon() {
      let args = {
        include: 'couponBatch',
        qiniu_id: this.id,
        oid: this.oid,
        belong: this.$route.query.utm_campaign
      }
      sendCoupon(args, this.coupon_batch_id)
        .then(res => {
          console.log('sendCoupon', res)
          this.handleData(res)
        })
        .catch(err => {
          alert(err.response.data.message)
        })
    },
    //处理返回数据
    handleData(res) {
      this.qrcodeImg = res.qrcode_url
      if (parseInt(res.status) === 1) {
        //已使用
        this.hasUsed = true
      }
    }
  }
};
</script>

