// 底部的按钮
<template>
  <div v-if="shouldMenuShow" class="btb">
    <div
      v-for="(item, index) in this.menuCode"
      :key="index"
      class="bitem"
      @click="handleMenuClick(routes[item.index])"
    >
      <img v-if="currentRoute !== routes[item.index]" :src="labelImg[item.index]">
      <img v-if="currentRoute === routes[item.index]" :src="labelImgPressed[item.index]">
      <span>{{labels[item.index]}}</span>
    </div>
  </div>
</template>

<script>
export default {
  name: "mSiteBottomBar",
  props: {
    // 是否替换默认menucode
    // 传入 Prop 则自定义菜单
    replaceMenuCode: {
      type: String,
      default: "",
      required: false
    },
    // 在没有mkey的情况下需要传入
    replaceMkey: {
      type: String,
      default: "",
      required: false
    },
    // default 显示默认菜单
    // replace 则替换
    replaceMode: {
      type: String,
      default: "default",
      required: false
    }
  },
  data() {
    return {
      labelImg: [
        "https://cdn.exe666.com/fe/image/m/btn_photo_normal@3x.png",
        "https://cdn.exe666.com/fe/image/m/btn_sale_normal@3x.png",
        "https://cdn.exe666.com/fe/image/m/btn_mall_normal@3x.png",
        "https://cdn.exe666.com/fe/image/m/btn_card_normal@3x.png",
        "https://cdn.exe666.com/fe/image/m/btn_my_normal@3x.png",
        "https://cdn.exe666.com/fe/image/m/barrage.png"
      ],
      labelImgPressed: [
        "https://cdn.exe666.com/fe/image/m/btn_photo_pressed@3x.png",
        "https://cdn.exe666.com/fe/image/m/btn_sale_pressed@3x.png",
        "https://cdn.exe666.com/fe/image/m/btn_mall_pressed@3x.png",
        "https://cdn.exe666.com/fe/image/m/btn_card_pressed@3x.png",
        "https://cdn.exe666.com/fe/image/m/btn_my_pressed@3x.png",
        "https://cdn.exe666.com/fe/image/m/barrage-p.png"
      ],
      showRoutes: [
        "MyIndex",
        "MallIndex",
        "CardIndex",
        "TrendsIndex",
        "ActivityShop",
        "BarrageIndex",
        "new_blue",
        "new_red",
        "new_yellow",
        "lkf",
        "newDreamland"
      ],
      labels: ["照片", "活动", "弹幕", "商城", "卡包", "我的"],
      routes: [
        "TrendsIndex",
        "ActivityShop",
        "BarrageIndex",
        "MallIndex",
        "CardIndex",
        "MyIndex"
      ]
    };
  },
  computed: {
    menuCode() {
      // if mcode should Repalce by Prop
      let { mcode } = this.$route.params;
      if (this.replaceMenuCode !== "") {
        mcode = this.replaceMenuCode;
      }

      // change 32 to 10 and fixed whith 0
      let ten = parseInt(mcode, 32).toString(10);
      let padNum = this.padNumber(ten, this.labels.length);

      // loop to return index
      let arr = [];
      this.labels.forEach((element, index) => {
        if (padNum[index] !== "0") {
          arr.push({
            index: index,
            order: Number(padNum[index])
          });
        }
      });
      arr.sort(function(a, b) {
        return a.order - b.order;
      });

      return arr;
    },
    currentRoute() {
      return this.$route.name;
    },
    shouldMenuShow() {
      if (this.replaceMode === "default") {
        return this.showRoutes.includes(this.$route.name);
      } else {
        return true;
      }
    }
  },
  methods: {
    padNumber(num, fill) {
      var len = ("" + num).length;
      return Array(fill > len ? fill - len + 1 || 0 : 0).join(0) + num;
    },
    handleMenuClick(routerName) {
      let { mkey, mcode } = this.$route.params;

      if (this.replaceMkey !== "") {
        mkey = this.replaceMkey;
      }

      if (this.replaceMenuCode !== "") {
        mcode = this.replaceMenuCode;
      }

      this.$router.push({
        name: routerName,
        params: {
          mkey: mkey,
          mcode: mcode
        }
      });
    }
  }
};
</script>

